    {
        $jenissales = new Jenissale();
        $jenissales->id_periode = $_SESSION["idperiode"];
        $jenissales->id_users = $_SESSION["iduser"];
        $jenissales->nama = $request->get('jenissales');
        $jenissales->persentase = $request->get('persentasesales');
        $jenissales->save();
        return redirect('periode');
    }

